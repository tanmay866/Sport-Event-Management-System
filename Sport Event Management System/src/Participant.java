// Participant.java
public class Participant {
    String name;       // Name of the participant
    String gender;     // Gender of the participant ("Male" or "Female")
    int score;         // Score of the participant

    // Constructor to initialize a participant
    public Participant(String name, String gender, int score) {
        this.name = name;
        this.gender = gender;
        this.score = score;
    }
}